// Operator
// 1. 산술 연산자 (Arithmetic Operator)
// + (더하기), - (빼기), * (곱하기), / (나누기), % (나머지)
let a = 10;
let b = 2;

console.log(a + b); // 12
console.log(a - b); // 8
console.log(a * b); // 20
console.log(a / b); // 5
console.log(a % b); // 0

// 2. 문자열 연산자 (String Operator) : 'String' + 'String', `${String}`
let add = "Hello";
let addString = "World";

console.log(add + " " + addString);
console.log(`${add + " " + addString}`);

// 3. 대입 연산자 (Assignment Operator)
// A = B : A = B
// A += B : A = A+B
// A *= B : A = A*B
// A /= B : A = A/B
// A %= B : A = A%B

// 4. 증감 연산자 (Increment and decrement Operator)
// Valable++;, ++Valable;, Valable--;, --Valable;

// 5. 비교 연산자 (Comparison Operator)
// A > B
// A < B
// A >= B
// A <= B
// A == B, A === B(자료형도 비교) : 같다
// A != B, A !== B(자료형도 비교) : A와 B는 다르다.

// 5. 논리 연산자 (Logical Operator)
// || : or 하나라도 true면 true.
// && : and 하나라도 false면 false.
// ! : not Operator, 결과의 반대값 출력.

// 6. 삼항 조건 연산자 (Ternary Operator)
// 조건식 ? 코드 1 : 코드 2
// true = 코드 1
// false = 코드 2

